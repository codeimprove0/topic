######### 4 Video 

1) h :- Move cursor to left by one position
2) l :- Move cursor to right by one position
3) k :- Move cursor to upward direction by one line
4) j :- Move cursor to downward direction by one line
  nth +j 
  
5) 0 zero :- Move cursor to the beginning of current line
6) $ :- Move cursor to the end of current line
7) ctrl +f :- Scroll down entire page
8) ctrl +b :- Scroll up entire page

9) :n :- Jump to the nth line
10) :0 :- Jump to the start of file
11) :$ :- Jump to the end of file

12) w :- Move cursor to the beginning of the next word
13) e :- Move cursor to the end of the current word
14) b :- Move cursor to the beginning of the previous word


########## 5 Video 

